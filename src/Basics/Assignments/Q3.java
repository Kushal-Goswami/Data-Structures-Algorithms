package Basics.Assignments;

public class Q3 {
    public static void main(String[] args) {
        //Integer result

        int num1=5;
        int num2=10;
        System.out.println("Integer result "+(num1+num2));

        //Float result

        float a=2.6f;
        float b=5.7f;
        System.out.println("Float result "+(a+b));

        //Double result

        double c=12.5;
        double d=2.8;
        System.out.println("Double result "+(c+d));

        //Division result

        double q1=10;
        double q2=2;
        System.out.println("Division result "+(q1/q2));
    }
}
